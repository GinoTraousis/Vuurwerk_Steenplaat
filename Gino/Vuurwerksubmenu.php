<p>          <h1>Wel/Geen vuurwerk?</h1>

<h3>Verbod</h3>
Wel of geen vuurwerk? Dat is de vraag van vele mensen in het land. Elk jaar hoor je wel problemen dat ontstaat door vuurwerk, alleen zijn het de laatste jaren er steeds meer zoals: vuurwerk dat naar politiebusjes worden gegooid, steeds meer illegaal vuurwerk wat voor vernielingen bezorgd en het grootste probleem is de herrie die vuurwerk veroorzaakt. 
<br> 
<br>
<h3>Milieu</h3>
Ook is er de laatste tijd een probleem met stikstof en vuurwerk verergert dat de problemen. Door het afsteken van vuurwerk komen er chemische stoffen en zware metalen in lucht, bodem en grondwater terecht. Op oud en nieuw is het in de steden de lucht 30 keer zo slecht.
<br>
<br>
<h3>Veiligheid </h3>
Tijdens de jaarwisseling eindigen jaarlijks bijna 500 mensen gewond, valt gemiddeld één dode per jaar en worden zo’n 11.000 incidenten geregistreerd. Het is in Nederland de onveiligste feest van het jaar. 
<br>
<br>
<h3>Mijn mening</h3>
Mijn mening is dat vuurwerk verboden moet worden. Omdat het voor veel overlast zorgen voor de mens en voor de dieren. Voor de dieren is het allerergst omdat zij niet weten wat er gebeurd en dan heel erg schrikken. Veiligheid is natuurlijk ook een van de reden waarom ik vindt dat het verboden moet worden zoals hier boven staat want er jaarlijks tienduizend incidenten. En de stikstof is al een groot probleem met de auto’s en vuurwerk verergert het alleen maar meer.
</p>



